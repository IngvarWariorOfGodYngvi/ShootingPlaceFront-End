<p class="text-left">1. Opis strzelnicy:<br/>
a) Strzelnica usytuowana jest w miejscowości Panaszew 4a, 99-200 Poddębice.<br/>
b) Strzelnica jest obiektem sportowym zamkniętym, przeznaczonym do prowadzenia szkoleń, treningów, zawodów i rekreacji. Może być udostępniona osobom posiadającym własną broń, jak też dysponującym bronią na podstawie innych przepisów.<br/>
c) Dozwolone jest strzelanie z broni: pneumatycznej, palnej bocznego i centralnego zapłonu, broni gładko lufowej.<br/>

2. Zadania strzelnicy<br/>
a) Pielęgnacja i propagowanie tradycji strzelectwa sportowego.<br/>
b) Nauka strzelania.<br/>
c) Doskonalenie umiejętności strzeleckich z różnych rodzajów broni.<br/>
d) Podnoszenie kultury strzeleckiej oraz współzawodnictwa sportowego (zawody, treningi, szkolenia, kursy).<br/>
e) Sprawdzanie, testowanie i przystrzeliwanie broni.<br/>

3. Ze strzelnicy mogą korzystać<br/>
a) Osoby posiadające własną broń z ważnym pozwoleniem.<br/>
b) Osoby pełnoletnie posiadające ważny dowód tożsamości.<br/>
c) Osoby niepełnoletnie w obecności instruktora, trenera, rodziców lub opiekunów lub za ich pisemną zgodą.<br/>
4. Funkcjonariusze Policji, Służby Więziennej, Specjalistycznych Uzbrojonych Formacji Ochronnych (SUFO), Wewnętrznych Służb Ochrony (WSO), Straży Granicznej<br/>
i innych służb mundurowych, z broni służbowej bądź obiektowej, po okazaniu ważnej legitymacji.<br/>
5. Zorganizowane grupy strzelających z własnym „prowadzącym strzelanie” pod warunkiem przedstawienia imiennej listy strzelających. Lista ta winna zawierać dane zgodne z Rozporządzeniem Ministra Spraw Wewnętrznych i Administracji z dnia 15.03.2000r. rozdz. 1, p.1.3. (Dz. U. Nr 18, poz. 234).<br/>

4. Obowiązki korzystającego ze strzelnicy<br/>
a) Zapoznać się i bezwzględnie przestrzegać Regulaminu Strzelnicy oraz Przepisów Bezpieczeństwa.<br/>
b) Dokonać czytelnego wpisu wymaganych danych do Książki rejestru pobytu na strzelnicy.<br/>
c) Bezwzględnie stosować się do komend i poleceń wydawanych przez prowadzącego strzelanie.<br/>
d) Tak kierować ogniem broni aby nie dopuścić do zagrożenia bezpieczeństwa osób przebywających na strzelnicy i do uszkodzeń urządzeń.<br/>
e) Należy używać środków ochrony słuchu i wzroku. Ich posiadanie należy do obowiązków strzelającego.<br/>
f) Bezwzględnie zdać niewykorzystaną amunicję – w przypadku strzelania z broni obiektowej.<br/>
</p>
<h6>ROZDZIAŁ 7<br/>Przepisy końcowe</h6>
<p class="text-left">1. Każda osoba znajdująca się na strzelnicy zobowiązana jest do zapoznania się z regulaminem, warunkami bezpieczeństwa i sygnałami alarmowymi.<br/>
2. Za szkody powstałe podczas strzelania oraz spowodowanie wypadku odpowiada właściciel lub zarządca strzelnicy, prowadzący strzelanie lub trening strzelecki albo korzystający ze strzelnicy, na zasadach określonych w odrębnych przepisach.<br/>
3. Korzystający ze strzelnicy zobowiązany jest do sprawowania nadzoru nad bronią i sprzętem wniesionym na teren obiektu.<br/>
4. Regionalne Centrum Strzeleckie Panaszew zlokalizowane w miejscowości Panaszew 4a,<br/>
99-200 Poddębice nie ponosi odpowiedzialności za sprzęt i rzeczy osobiste pozostawione na terenie obiektu bez nadzoru ich właściciela.<br/>
5. Osoby posiadające własną broń zobowiązane są do zgłoszenia prowadzącemu strzelanie, rodzaju broni i ilości amunicji wnoszonej na strzelnicę.<br/>
6. W czasie oczekiwania na swoja kolejność strzelania zabrania się jakichkolwiek czynności z bronią.<br/>
</p>
<h6>PRZEPISY BEZPIECZEŃSTWA W STRZELECTWIE SPORTOWYM<br/>OBOWIĄZUJĄCE NA STRZELNICY</h6>
<p class="text-left">1. Każdą bronią należy zawsze posługiwać się z najwyższą ostrożnością i traktować ją jak załadowaną.<br/>
2. Nigdy nie kieruj wylotu lufy karabinu, pistoletu lub strzelby w kierunku ludzi.<br/>
3. Każdorazowo po pobraniu broni do ręki, sprawdź czy jest rozładowana (najpierw skieruj ją w bezpieczne miejsce, odłącz magazynek, następnie sprawdź komorę nabojową)<br/>
4. W czasie odpoczynków i innych przerw w strzelaniu nigdy nie odkładaj broni załadowanej.<br/>
5. Na strzelnicy broń przenoś zawsze rozładowaną, zabezpieczoną i schowaną w kaburze, pokrowcu lub kasecie przeznaczonej do przenoszenia broni.<br/>
6. Wyjmowanie broni odbywa się wyłącznie na stanowisku strzeleckim lub treningowym tylko na polecenie prowadzącego strzelanie lub trening strzelecki.<br/>
7. Poza strzelnicą lub magazynem, broń przenoś w sposób wykluczający jej samoczynne zadziałanie.<br/>
8. Nigdy nie pozostawiaj broni bez dozoru.<br/>
9. Broń ładuj wyłącznie na stanowisku strzeleckim na linii ognia, lufą skierowaną w kulochwyt, prostopadle do linii tarcz i tylko po komendzie prowadzącego ŁADUJ lub START.<br/>
10. Przy ładowaniu broni nie korzystaj z pomocy innych osób.<br/>
11. W czasie ładowania i strzelania wylot lufy musi znajdować się przed linią ognia (patrząc od linii tarcz), przestrzegając jednocześnie przepisów dotyczących postaw strzeleckich.<br/>
12. Po komendzie S T O P wydanej przez prowadzącego strzelanie lub inną osobę, strzelający bezzwłocznie przerywa strzelanie.<br/>
13. W przypadku gdy kontynuowanie strzelania będzie zagrażało życiu ludzi, komendę STOP powinien wydać każdy, kto pierwszy zauważy niebezpieczeństwo.<br/>
14. Komenda rozładuj, nakazuje natychmiast rozładować broń i z widoczną, pustą komorą nabojową umieścić na stanowisku strzeleckim.<br/>
15. Nigdy nie strzelaj z broni niesprawnej i niezapewniającej bezpieczeństwa.<br/>
16. Zabronione jest dotykanie cudzej broni bez zezwolenia użytkownika lub właściciela.<br/>
17. W czasie trwania strzelania zabronione jest wychodzenie przed linię ognia.<br/>
18. Po zakończeniu strzelania, przed opuszczeniem stanowiska strzeleckiego, broń musi być rozładowana i odpowiednio do jej typu zabezpieczona.<br/>
19. Zanim opuścisz stanowisko, upewnij się, czy w komorze nabojowej i magazynku nie pozostał nabój, następnie przedstaw rozładowaną broń do kontroli prowadzącemu strzelanie i odpowiednio ją zabezpiecz.<br/>
20. Składanie się, celowanie i strzelanie „ na sucho ” dozwolone jest na stanowisku strzeleckim lub tylko w miejscach do tego wyznaczonych.<br/>
21. Na strzelnicy nie można celować i strzelać do ludzi, ptaków i zwierząt, jak również do przedmiotów znajdujących się poza kulochwytem.<br/>
22. Naprawy, regulacje i czyszczenie broni pociągające za sobą konieczność rozkładania jej, należy dokonywać w miejscach do tego wyznaczonych.<br/>
23. Wszyscy strzelcy i inne osoby znajdujące się w bezpośrednim sąsiedztwie linii ognia powinny używać wkładek, nauszników lub innych urządzeń służących ochronie słuchu.<br/>
24. Zabronione jest używanie przez strzelców w czasie strzelania, ochraniaczy słuchu podłączonych do jakichkolwiek urządzeń odbiorczych.<br/>
25. Na strzelnicy, posiadacze telefonów komórkowych powinni w swoich aparatach wyłączyć sygnały dźwiękowe.<br/>
26. Podczas strzelania wszyscy strzelcy powinni używać bezodpryskowych okularów strzeleckich lub innych podobnych środków chroniących aparat wzroku.<br/>
